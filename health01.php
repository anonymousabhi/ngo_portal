      <p>Goonj, formed in 1999 by Anshu Gupta, started with 67 clothes and now deals with over 3500 tonnes of material every year. It is registered under Societies Act and registered for exemption under section 80 G, 12 A and for foreign contributions- under FCRA.

Anshu’s own epiphany came in meeting a poorly-clad six-year- old girl who grew up with corpses because her father eked out a living picking up abandoned dead bodies and cremating them for a fee. When he asked the girl what she did to avoid the cold in Delhi’s harsh winter, she said: “When I feel cold, I hug a dead body and sleep.” 

The 2004 tsunami gave way to Goonj's 'Not Just a Piece of Cloth' (NJPC) to emerge. The Chennai center was flooded with old but usable clothes during the relief phase. These clothes were then disinfected, washed, and recycled into pads and then placed in cotton bags to form Goonj's Dignity Kit. By 2014, 4 million pads just distributed at nominal cost of ₹2 (2.9¢ US),  to the poor who cannot afford commercial sanitary pads. The campaign subsequently won the World Bank's Global Development Marketplace Award (2009) and Changemaker's Innovation Award (2009), and was LAUNCH Innovation Challenge in 2012, in "Beyond Waste" theme, which is sponsored by NASA, Nike and US Agency for International Development. 

In 2009, it was one of the first organizations to join the Joy of Giving Week, later renamed Daan Utsav (Donation Festival), an annual week-long donation drive, starting Gandhi Jayanti October 2–8. During the week Goonj runs a nationwide campaign, where people from across India contribute under-underutilized goods and clothes from their homes, offices and schools in urban area to be distributed to rural and poor areas.</p>

